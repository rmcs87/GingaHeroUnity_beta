    // Método que retorna a próxima
    public int[] getNextNote()
    {
        if(notaAtual >= numNotas)
        {
            return null;
        }
        int [] nota = new int[] { notas[notaAtual, 0], notas[notaAtual, 1],
                           notas[notaAtual, 2], notas[notaAtual, 3] };
        notaAtual++;
        return nota;
    }

    //Método que atualiza a notaAtual para uma nota específica
    public void jumpToNote(int n)
    {
        notaAtual = n;
    }

    // Método que re-inicializa para a primeira nota
    public void restart()
    {
        notaAtual = 0;
    }
